              <p>Now that she is comfortable with both her faith and her sexuality, Kirsten said she wants to focus on spreading God's love. </p>

              <p>"God has blessed me so much, I can't deny his presence and I need to continue to filter his love and to just spread that to other people so that they can feel loved as well," Kirsten said.</p>

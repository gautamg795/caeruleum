            <h3>Michael Hirshman</h3>
            <section>
                <p dir="ltr">The role of Graduate Students Association president requires reliable connections and vast knowledge of the organization’s procedures – experience that candidate Michael Hirshman lacks.</p>
                <p dir="ltr">The Anderson School of Management student showed admirable enthusiasm about expanding GSA’s reach on campus and increasing interaction among the different graduate schools. But without a more expansive background with student government, this board believes that Hirshman would struggle to realize his goals.</p>
                <p dir="ltr">When asked about his experience, Hirshman pointed to previous management positions at venture capital and investment companies as well as his active involvement in undergraduate student government at UCSD. Such experience, while valuable, does not indicate knowledge of the inner workings of GSA, knowledge critical for the association's president.</p>
                <p dir="ltr">Hirshman’s opponent, current GSA President Nicole Robinson, has spent multiple years with the organization and showed the ability to bring her goals to fruition. Robinson, who served as vice president for academic affairs before her current position, showed a greater effort to educate herself about the organization before running for its top position.</p>
                <p dir="ltr">In an interview with the editorial board, Hirshman spoke at length about many admirable goals, such as incentivizing professors to prioritize teaching over research and providing better workers’ compensation for graduate student researchers, but left out concrete plans about how to make them happen.</p>
                <p dir="ltr">Hirshman said that he thought his presence on GSA as a student from a professional school would add some much-needed diversity to the organization and he hopes to do more outreach to all types of graduate students. In this area, we believe Hirshman draws much-needed attention to the fractured, or “siloed,” nature of the graduate student body, which by nature of its role in academic life tend toward focusing intently on its departments and laboratories.</p>
                <p dir="ltr">Hirshman’s ambitions are important and worthwhile for GSA, which seems to be losing relevance on campus, but they can be accomplished outside of the president’s office.</p>
                <p dir="ltr">This board applauds Hirshman’s enthusiasm and, whether elected or not, we hope he will bring his ideas to GSA next year.</p>
            </section>

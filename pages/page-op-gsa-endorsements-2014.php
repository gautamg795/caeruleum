            <h3>Vaheh Shirvanian</h3>
            <section>
                <p dir="ltr">Perhaps the most pressing task of the Graduate Students Association vice president of external affairs is understanding the concerns of the organization’s constituents and communicating them to audiences both internal and external.</p>
                <p dir="ltr">Vaheh Shirvanian, a candidate for GSA’s external affairs position, lacks the necessary knowledge to adequately advocate for the concerns of graduate students on campus.</p>
                <p dir="ltr">When asked about outreach to different groups on campus and about the complex set of graduate student issues an external vice president must communicate, Shirvanian expressed confusion on most of the matters concerning the office.</p>
                <p dir="ltr">For example, Shirvanian could not name any groups he would like to reach out to as vice president of external affairs.</p>
                <p dir="ltr">He also did not seem to have a clear grasp of his slate’s platforms, aside from the general notion of joining the UC Student Association.</p>
                <p dir="ltr">He offered fossil fuel divestment as part of his platform for cooperation and collaboration with the undergraduate student government, but seemed unaware of administrative opposition within the University of California that has in the past stymied efforts to use the system’s investment for political purposes.</p>
                <p dir="ltr">Shirvanian did express a deep commitment to learning what he did not know, and his forthrightness about his lack of expertise was commendable. But when being considered for office, a background of experience and intricate knowledge of graduate students must take precedence over a dose of honesty and enthusiasm.</p>
                <p dir="ltr">As a first-year graduate student, Shirvanian has plenty of time ahead of him to better acquaint himself with GSA. Shirvanian is smart and well-spoken, and he displays initiative. With a year of experience under his belt, he could be a strong contender for a GSA position.</p>
            </section>

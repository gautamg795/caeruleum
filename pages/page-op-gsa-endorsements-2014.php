            <h3>Nicole Robinson</h3>
            <div class="alert alert-success endorsed"><h4><img src="//dailybruin.com/images/gsa-endorsements-2014/check.png" class="check-big"> Endorsed</h4></div>
            <section>
                <p dir="ltr">Both presidential candidates for next year’s Graduate Students Association expressed ambitious goals for the organization, including an expansion of financial support for graduate students and measures to increase diversity and inclusion.</p>
                <p dir="ltr">This board endorses current GSA President Nicole Robinson for re-election because her combination of experience and vision makes her the most qualified to accomplish these goals.</p>
                <p dir="ltr">Robinson is a long-standing member of the graduate student government, having served as vice president of academic affairs before her current post as president. During this time, she helped better the terms of a new leave of absence policy and successfully lobbied graduate students for more funding for the Graduate Writing Center.</p>
                <p dir="ltr">Both accomplishments demonstrate her ability to navigate the multifaceted UCLA institution to secure her constituents’ best interests.</p>
                <p dir="ltr">The adage “don’t change horses in midstream” comes to mind when considering this year’s presidential race. Robinson’s opponent, Michael Hirshman, shows promise as a leader and has a strong set of organizational skills that come from professional experience. But ultimately, Robinson is better equipped to maneuver the organization into a position of increased prominence and to lobby for graduate student issues.</p>
                <p dir="ltr">In her interview with the editorial board, Robinson displayed a keen understanding of the bodies that GSA must work with to accomplish its foundational goals. Her platforms of increasing financial support for graduate students and consolidating and expanding career development programs are bolstered by contacts she already has within the Graduate Division, the Career Center and the Graduate Student Resource Center.</p>
                <p dir="ltr">In addition, Robinson’s network of political connections include members of the Undergraduate Students Association Council. Besides contributing $5,000 of GSA funds to a mobile safety application being established by the USAC internal vice president, Robinson signed on to important undergraduate initiatives such as 7,000 in Solidarity and the Invest in Graduation Not Incarceration, Transform Education campaign.</p>
                <p dir="ltr">In endorsing Robinson, we encourage her to continue to leverage the enthusiasm of undergraduate students in furthering the interests they share with graduate students. Perhaps Robinson can add the goal of reducing class sizes to these shared initiatives.</p>
                <p dir="ltr">The one segment of campus to which Robinson must devote her attention in a second term is professional students, who, though a voting sector of GSA, often feel disenfranchised from the organization. In particular, many professional students opposed Robinson’s push for a referendum to fund the Graduate Student Writing Center.</p>
                <p dir="ltr">Robinson can bring into the fold students from the UCLA School of Law and the Anderson School of Management, among others, by engaging her opponent, an Anderson student, in effective outreach methods to students at professional schools. Beyond that, she must make an effort to be attentive to the needs of professional students and make GSA work for their best interests.</p>
                <p dir="ltr">Another year with Robinson in GSA’s top spot would be sure to serve the UCLA graduate community well and would allow her to carry valuable momentum from this year to next year.</p>
            </section>

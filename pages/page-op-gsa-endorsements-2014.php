        <div class="large-9 columns">
            <h3>Ivy Onyeador</h3>
            <div class="alert alert-success endorsed"><h4><img src="//dailybruin.com/images/gsa-endorsements-2014/check.png" class="check-big"> Endorsed</h4></div>
            <section>
                <p dir="ltr">This board endorses Ivy Onyeador for vice president of academic affairs for her comprehensive vision to foster a sense of community among graduate students and her enthusiastic approach to securing more funding resources to support graduate students.</p>
                <p dir="ltr">One of Onyeador’s most crucial platforms for next year is fostering the growth of diverse communities and bringing them into closer cooperation with the Graduate Students Association – in the process improving the academic experience of underrepresented students at UCLA. This board believes that with her past experience, she can make great strides toward that goal.</p>
                <p dir="ltr">Onyeador’s work with identity-based groups – such as the Black Graduate Students Association and the La Raza Law Students Association – places her in a position to help bridge the gap between GSA and student organizations. By creating what she called “smaller communities” within the large and disparate system of UCLA graduate schools, Onyeador will be able to promote academic success through social connections and networking.</p>
                <p dir="ltr">As Onyeador stressed, comfort and inclusion are necessary conditions for graduate students to excel in their scholarship. The goal of community-building in the graduate school cannot be overstated and is particularly important given charged incidents in multiple graduate departments that demonstrated feelings of disenfranchisement among some underrepresented communities.</p>
                <p dir="ltr">While we strongly believe she is capable of building student networks around identities rather than departments – thus tackling the problem of students’ being secluded within their academic fields – Onyeador’s conception of her office leaves a bit to be desired.</p>
                <p dir="ltr">In order to ensure she makes progress during her time as vice president of academic affairs next year, Onyeador needs to learn more about her role in the Graduate Council and more generally as a voice in the UCLA Academic Senate.</p>
                <p dir="ltr">She aims to increase funding for graduate students through better fellowship opportunities and teaching assistant positions, a rather ambitious but important goal for the office she seeks. In addition, as vice president she will be responsible for coordinating graduate student support for a diversity-related undergraduate general education requirement.</p>
                <p dir="ltr">Right now she lacks a comprehensive knowledge of curriculum formation and faculty governance at UCLA, which is critical know-how to fully realize these goals.</p>
                <p dir="ltr">Still, Onyeador showed initiative and potential to do realistic work toward better financial support. We believe that Onyeador can learn quickly what it takes to be an influential voice in the Academic Senate.</p>
            </section>

            <h3>Andrés Schneider</h3>
            <div class="alert alert-success endorsed"><h4><img src="//dailybruin.com/images/gsa-endorsements-2014/check.png" class="check-big"> Endorsed</h4></div>
            <section>
                <p dir="ltr">This board endorses Andrés Schneider for vice president of external affairs because of his great enthusiasm for service and plans to involve a greater number of his peers in student government.</p>
                <p dir="ltr">Schneider, a graduate student in economics, previously worked as the Graduate Students Association’s discretionary funding director – experience we believe will equip him to succeed in opening up GSA funds to a greater number of student groups.</p>
                <p dir="ltr">The board was impressed by Schneider’s emphasis on improving the association’s transparency by presenting the institution’s budgets and available funds online. If elected, Schneider would do well to forge ahead with this platform over the summer and engage other GSA leaders in identifying projects of even greater scale to improve the group’s presentation of information to its community.</p>
                <p dir="ltr">Schneider also emphasized outreach to a range of communities on campus.</p>
                <p dir="ltr">In particular, Schneider discussed increasing communication between GSA and its undergraduate counterpart, the Undergraduate Students Association Council. We hope Schneider is able to realize this platform, and if he secures office, we strongly encourage him to set up a method of regular communication between student governments as soon as the new USAC council is elected.</p>
                <p dir="ltr">Consistency will be the most difficult aspect of establishing a link with undergraduates, but one that could yield great results for both bodies.</p>
                <p dir="ltr">Communication with USAC is particularly important because, during his interview, Schneider said he “understands perfectly” the problems and realities of undergraduates, an assertion that needs to be backed up with action and visible collaboration. While Schneider’s experience as a teaching assistant has surely exposed him to undergraduates in an academic setting, comprehending the issues of undergraduates must be an ongoing process.</p>
                <p dir="ltr">Another group Schneider can make inroads with is the sizable international student population, of which he is a member.</p>
                <p dir="ltr">In his time at UCLA, Schneider acted as a student ambassador for Argentina with the Dashew Center for International Students and Scholars. We believe Schneider’s experience as a student leader with the Dashew Center and understanding of the diversity of the international student experience will be an asset to the body.</p>
                <p dir="ltr">Finally, Schneider spoke of the importance of renewing GSA’s affiliation with the UC Student Association, a UC-wide advocacy organization. While many candidates presented joining UCSA as a platform, Schneider promised to make sure that UCLA’s graduate student government will play a big part in the organization’s conversation despite its new membership.</p>
                <p dir="ltr">We are confident Schneider will be a productive and charismatic member of the GSA council and encourage him to make good on his promises of improving the organization’s transparency and engaging communities across campus.</p>
            </section>

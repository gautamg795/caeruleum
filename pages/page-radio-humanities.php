			<p><b>ALARKON:</b> A few years ago, six of UCLA’s European language departments almost became one super-department in a bid to streamline the humanities.</p>
			<p>In 2009, a task force put together by then-executive vice chancellor Scott Waugh proposed to make teaching the humanities more efficient. The task force wanted to address budget cuts and improve the way UCLA taught the discipline.</p>
			<p>Changes were implemented within the Department of Spanish and Portuguese, which have affected students in introductory and intermediate courses – courses such as Spanish 1 through 5, and Spanish 7A and 7B.</p>
			<p>They’re now being taught as hybrid programs, which sees students spending more time online and less time in the classroom.</p>
			<p><b>ALARKON:</b> Ezekiel Trautenberg is a TA for Spanish 5, an intermediate-level course.</p>

			<div class="image">
				<img src="//dailybruin.com/images/2016/03/royce.jpg">
				<span class="caption">(Katrina Alarkon/Daily Bruin)</span>
			</div>

			<p><b>TRAUTENBERG: </b> We were teaching five days a week. Now we’re spending about two and a half hours in the classroom. There’s a little bit more of an online component and some of their exams are online.
			</p>
			<p><b>ALARKON:</b> He says other universities have adopted a similar approach when it comes to teaching language courses as more students sign up for them. At UCLA, it’s allowed more people to take classes that previously weren’t available to them.
			</p>
			<p><b>TRAUTENBERG: </b> I think it’s kind of inevitable as there’s more demand for language courses. What we do in the classroom hasn’t really changed but students expect it now to have some kind of online component. The real challenge is to find the balance.
			</p>
			<p><b>ALARKON:</b> Melanie Gomez is the final year of her political science major. She’s one of Trautenberg’s students and she says she completed some early Spanish classes.
			</p>
			<p><b>GOMEZ:</b> Thirty-five years ago, we didn’t have the online access. You had your book, you had your exercises in the book, and there wasn’t a lot of interactive practice.
			</p>
			<p><b>ALARKON:</b> She says the digital approach has changed the way she’s learning.
			</p>
			<p><b>GOMEZ:</b> It’s more interactive, it makes it a lot more fun, a lot more interesting. You can cover more material.
			</p>
			<p><b>ALARKON:</b> Mariska Bolyanatz says she tries to make the most of the time she’s given as a TA for Spanish 5.
			</p>
			<p>What face-to-face class time she gets gives her a chance to make sure students are getting the best learning experience possible.</p>

			<p><b>BOLYANATZ:</b> What we want to spend time on in class are what we call communicative activities. So practicing reading, or practicing speaking, or practicing listening and writing. All the things that you have to do when you communicate in a language.
			</p>
			<p><b>ALARKON:</b>She says her students have become more autonomous when it comes to learning the material she assigns.</p>

			<p><b>BOLYANATZ:</b> They have to really take responsibility for their learning process in terms of their preparation work in a way that I didn’t see my students necessarily doing before when it was a non-hybridized program.</p>

			<p><b>ALARKON:</b> … And she thinks this type of autonomy is important because...</p>

			<p><b>BOLYANATZ:</b> That’s something that I don’t think is necessarily encouraged in other classes and even perhaps in the model that we were using before. The sort of "five days a week, you come to class, I teach you what you need to know." That’s not actually how it works. You put in the work, you come into class and we practice.</p>

			<p><b>ALARKON:</b> When the proposal to merge the European language departments was announced, people worried cutting corners financially would hurt the quality of education and take away agency from smaller departments. Despite the merge being announced years ago, the six departments still stand separately today. And in the meantime, teaching these hybrid courses appears to keep this threat at bay.</p>

			<p>For the Daily Bruin, I’m Katrina Alarkon.</p>


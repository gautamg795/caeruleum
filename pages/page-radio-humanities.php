			<h3>Humanities enrollment remains steady amid department changes</h3>

			<h4>By Douglas Hsieh</h4>

			<div class="audio">
				<iframe width="100%" height="450" scrolling="no" frameborder="no" src="https://w.soundcloud.com/player/?url=https%3A//api.soundcloud.com/tracks/251091521&amp;auto_play=false&amp;hide_related=false&amp;show_comments=true&amp;show_user=true&amp;show_reposts=false&amp;visual=true"></iframe>
			</div>

			<p><b>Hsieh:</b> Type in these three words into Google: “Are the humanities …” and you get a list of what people search for when they ask about the humanities. Among these suggestions include: “Are the humanities important, dying, useless.” The list goes on and on. On the surface at least, there is a serious concern over the role of the humanities in our technologically driven society.</p>

			<p>Take 2011. UCLA. The humanities division faced a severe budget deficit of $4 million to $5 million dollars. So severe that the Academic Senate proposed merging the European language departments into a single department to keep costs down.</p>

			<p>But were things as grim as they appeared? Assistant dean of the humanities, Reem Hanna-Harwell, doesn’t think so.</p>

			<p><b>Hanna-Harwell:</b>  So that particular budget problem was solved internally within the campus. And I should add, it’s not a problem that the humanities uniquely faced. It was part of a bigger problem that has been faced by most if not all of our academic units on campus. (5:28-5:57)</p>

			<div class="image">
				<img src="//dailybruin.com/images/2016/03/IMG_20160229_110849.jpg">
				<span class="caption">(Owen Emerson/Daily Bruin)</span>
			</div>


			<p><b>Hsieh:</b>  So if wasn’t a specific problem pertaining to the humanities, what were the actual causes of these problems?</p>

			<p><b>Hanna-Harwell:</b>  This is the main driver of those numbers. There was a structural shortfall on benefits. And also the retirement system was now requiring employers, meaning UCLA being one of the employers within the UC system, to pay into the retirement system. And these were staggering new costs for all academic units. (6:26-6:51)</p>

			<p><b>Hsieh:</b>  So according to Hannah-Harwell, what one might perceive as a trend of decline in funding of the humanities might not actually be a decline. In fact, since 2011 …</p>

			<p><b>Hanna-Harwell:</b>  I would say that funding has increased to the humanities, actually substantially. (7:17-7:22)</p>

			<p><b>Hsieh:</b>  But why? What actually drives funding in the humanities?</p>

			<p><b>Hanna-Harwell:</b> That has to do with the increase in our undergraduate student body that has happened since 2011. (7:23-7:31)</p>

			<p><b>Hsieh:</b>  But any budget would grow with more students enrolled. The real question here is: has funding in the humanities grown proportionally to other subjects?</p>

			<p><b>Hanna-Harwell:</b> Humanities have kept pace in terms of funding with other academic units on campus. (9:04 - 9:10)</p>

			<p><b>Hsieh:</b>  So what do the statistics show us? Well, according to the Office of Analysis and Information Management, from 2007 to 2013, the amount of undeclared students majoring within the humanities rose from about 270 students to 310 students. Compare that with mathematics, which grew from 855 students to 1584 students in the same span of time. So although we see a steady growth in the humanities, the humanities seem to be losing ground to the hard sciences. Professor emeritus James Catterall provides an explanation.</p>

			<p><b>Catterall:</b> The college freshman survey (15:05-15:07) Astin conducted over 35 years shows that students were becoming more and more interested in making money and less interested in doing things for personable causes. (15:13-23) Students coming in, they’re not majoring in history ... (15:45-15:47) They’re going for economics, science, mathematics, computer science, particularly information technology. They’re going where they perceive the money is and where the jobs are. (15:49-16:05)</p>

			<p><b>Hsieh:</b>  Although we see a clear trend to where students are prioritizing their education, the humanities are still growing to an extent. The diverse amount of languages we acknowledge in the European language departments are a testament to this fact.</p>

			<p>For Daily Bruin Radio, this is Douglas Hseih.</p>


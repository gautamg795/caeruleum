       <p>Growing up, Sophie Tanaka remembers seeing her mother break down in tears at a bank, overwhelmed by simple math calculations.</p>
       <p>A few years ago, her mom found enough confidence to return to community college to take a math class. Tanaka said seeing her mother struggle encouraged her to dispel the stereotype that women do not study science, technology, engineering or mathematics.</p>
       <p>Tanaka, a fourth-year psychology transfer student, took a job at the UCLA Luskin Center for Innovation to address the lack of diversity in leading technological companies and encourage executives to fairly promote women in workplaces.</p>
       <p>Tanaka is one of five students awarded the annual True Bruin Distinguished Senior Award. The award grants up to $5,000 to students who demonstrate integrity, respect and service during their academic careers.</p>
       <p>Tanaka has conducted research since she was in community college. When she arrived at UCLA, she decided to design a study that would evaluate psychological and social factors behind women&#8217;s absence in STEM fields.</p>
       <p>Women account for over 50 percent of college students, but are still a minority group in STEM fields, Tanaka said.</p>
       <p>&#8220;The two fields that are the most lucrative and have the most growth are the fields in which women are most underrepresented,&#8221; Tanaka said.</p>
       <p>Last year, the <a target="_blank" rel="nofollow" href="//www.ugresearchsci.ucla.edu/urfp.htm">Undergraduate Research Fellows Program</a> funded Tanaka&#8217;s research. She later presented the conclusions from her study, which found some job applications were worded to discourage women from applying to jobs in STEM fields.</p>
       <p>Rebecca Sadwick, program manager of the digital technologies initiative at the center for innovation, said she heard about Tanaka&#8217;s research and thought she&#8217;d be a good fit for her project, which aimed to analyze existing literature on women in STEM fields and compile strategies for industry leaders to increase diversity.</p>
       <p>Sadwick said Tanaka, the only undergraduate student working on the project, demonstrated skills and maturity that exceeded what she expected of her graduate students.</p>
       <p>&#8220;I was incredibly impressed by her intellect and passion,&#8221; Sadwick said. &#8220;She could jump from one complex issue to another.&#8221;</p>
       <p>Tanaka now works in a lab at UCLA with Steven Reise, Tanaka&#8217;s thesis adviser and a UCLA psychology professor who specializes in quantitative data.</p>
       <p>Reise said he thinks Tanaka is filling in a large gap in the research field by directing her honors thesis toward researching why individuals appear as outliers to the rest of a population and examining why inconsistencies occur in survey answers.</p>
       <p>Tanaka said she thinks stereotypes about women&#8217;s roles still exist and students who see themselves as poor performers in mathematics are scared of pushing themselves in that area.</p>
       <p>&#8220;I know my mom has this fear of math and wasn&#8217;t expected to be successful or good at it,&#8221; Tanaka said. &#8220;But I&#8217;ve watched her face her fear.&#8221;</p>
       <p>She added she could not have accomplished what she has without her mom. </p>
       <p>&#8220;(She) told me everything she wants to do, she&#8217;s had to struggle for,&#8221; Tanaka said. &#8220;For me, things come easier and I have to take advantage of that &#8211; I have to keep pushing myself and help those around me.&#8221;</p>
       <p>Tanaka said working on a psychological research project created by Philip Zimbardo, a professor emeritus of psychology at Stanford University who is known for conducting the Stanford Prison Experiment in 1971, at her community college inspired her to pursue a career in social psychology.</p>
       <p>She added she set her sights on UCLA to get her bachelor&#8217;s degree in psychology before moving on to earn a doctorate degree. Tanaka said she hopes her experiences at UCLA prepare her for her dream of becoming a research professor.</p>
       <p>&#8220;I&#8217;ve learned to embrace challenges,&#8221; Tanaka said. &#8220;I believe I can do anything I&#8217;m willing to work for.&#8221;</p>

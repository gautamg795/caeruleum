<p class="eh-author">by Katherine Hafner</p>
<p class="eh-published">October 14, 2013</p>

<div class="eh-infobox eh-aside-right eh-refer">
    <div><a href="//dailybruin.com/2013/10/17/a-constant-threat-uc-attempts-to-balance-digital-privacy-security/"><div class="eh-icon eh-icon-link"></div>UC attempts to balance digital privacy, security</a></div>
    <div><a href="//dailybruin.com/2013/10/17/professor-discusses-2006-ucla-computer-security-breach/"><div class="eh-icon eh-icon-speaker"></div>Listen: Professor discusses 2006 UCLA computer security breach</a></div>
</div>
<p>Earlier this summer, the UCLA Information Technology Security Office put up a new campus computer server &ndash; a routine practice.</p>
<p>Fifteen minutes later, there had been 60,000 hacking attempts.</p>
<p>Thousands of Stanford University students and network users had to change their passwords in July, after a massive hacking incident threatened data stored in its system &ndash; only months after a similar attack.</p>
<p>Cyber security incidents at these institutions are not isolated.</p>
<p>The threat of hacking is commonplace and on the rise at major universities nationwide.</p>
<p>Professional hackers around the globe work around the clock to try and infiltrate university systems and access individual financial information. Robots with the sole purpose of hacking into major college databases to steal student information or research run through millions of passwords a minute.</p>
<p>The trend has the University of California taking extra precautions.</p>

<h2 class="eh-header">On the rise</h2>
<img class="eh-aside-right" src="//dailybruin.com/images/2013/10/CL.Hacking.Data_.copyedited.ai_.jpg"/>
<p>The threat is increasingly feared and fought &ndash; a breach of security in which the personal information of students, faculty or staff, such as social security numbers or credit card information, is stolen and sold on the black market.</p>
<p>Earlier this year, The New York Times <a href="//www.nytimes.com/2013/07/17/education/barrage-of-cyberattacks-challenges-campus-culture.html?_r=0">published an article</a> detailing how hacking attempts on major universities across the U.S., especially from China, are becoming more frequent.</p>
<p>The New York Timesarticle garnered concern from some UCLA employees, said Ross Bollens, chief information security officer at UCLA.</p>
<p>&ldquo;I run around wringing my hands ... saying we should pay attention to this,&rdquo; Bollens said, laughing. &ldquo;That article, it was interesting, because I got an email from a vice chancellor saying &lsquo;Does this affect us?&rsquo; and four other emails ... It (finally) got their attention."</p>
<p>He added that the Stanford incident this summer further emphasized to university officials that hackers are attacking major universities.</p>
<p>Out of over 450 incidents of data breaches reported in 2013 so far, about 40 were at universities or university organizations, including Chapman University, the Columbia University Medical Center and Emory University, <a href="//www.idtheftcenter.org/images/breach/ITRC_Breach_Report_2013.pdf">according to the Identity Theft Resource Center</a>.</p>
<div class="eh-infobox eh-aside-left">
    <h3 class="eh-header">Did You Know?</h3>
    <p>UCLA network users can now have passwords as long as 128 characters. UCLA made the change so a user can even choose a sentence, such as a quote from their favorite book, as their password. Longer passwords are more secure.</p> 
    <p class="eh-credit">Source: Ross Bollens, chief information security officer at UCLA. Compiled by Katherine Hafner, Bruin senior staff.</p>
</div>
<p>The center defines a data breach as an instance in which an individual&rsquo;s Social Security Number, driver&rsquo;s license number, medical record or financial record, including credit and debit card information, is potentially put at risk.</p>
<p>As a university that houses important research information, UCLA is not only a likely, but also a constant &ndash; target Bollens said.</p>

<h2 class="eh-header">Breaches at UCLA</h2>
<p>Bollens has worked for the UCLA Information Security Office for seven years. In that time, he has witnessed a number of minor system break-ins and major security breaches.</p>
<p>The biggest breach of security UCLA has seen thus far occurred in 2006, when hackers attacked UCLA&rsquo;s administrative database, stealing thousands of Social Security Numbers.</p>
<img class="eh-fullwidth" src="//dailybruin.com/images/2013/10/Hacking_Timeline_copyedited.ai_.jpg"/>
<p>Bollens had just started working at UCLA in August 2006, and in November the office discovered that a hack had been in continual motion for almost a year. A month later, the office sent out an email to 800,000 people &ndash; including people who had only applied to the university &ndash; warning them that their Social Security information may be at risk.</p>
<p>The announcement sent shockwaves through university and IT communities. At the time, the 2006 breach was considered &ldquo;one of the largest computer security breaches ever at an American university,&rdquo; <a href="//articles.latimes.com/2006/dec/12/local/me-ucla12">according to the Los Angeles Times</a>.</p>
<p>UCLA even <a href="//dailybruin.com/2008/01/09/database-breach-investigation-ongoing/">called in the FBI to investigate</a>, who eventually discovered that the attack came from China, Bollens said.</p>
<p>He added that they later figured out only a portion of the larger net of people actually had their Social Security numbers stolen &ndash; about 20,000 people.</p>
<p>Bollens said the 2006 incident was one of the only examples of a real &ldquo;breach,&rdquo; in which personal information is threatened, at UCLA.</p>
<p>An instance at the UCLA Medical Center in 2008 involved an employee who had access to celebrity medical information and exploited her access to sell the information to a tabloid magazine.</p>
<img class="eh-aside-left" src="//dailybruin.com/images/2013/10/10.17.news_.cl_.hacking.breach.copyedited.ai_.jpg"/>
<p>Most of all, Bollens said he has seen an increase in schemes that try to dupe students, which can be easily avoided.</p>
<p>&ldquo;Because there are so many technological advances, the hackers have to resort to social engineering,&rdquo; he said. &ldquo;(They) have to get you involved as part of the scheme.&rdquo;</p>
<p>Bollens said one of the most common scenarios at UCLA involves an email supposedly coming from the UCLA Bruin OnLine office, asking students to input their username and password to verify them on a site that looks valid. But the link leads to a third-party web host set up by hackers to steal the information.</p>
<p>He added that the Bruin OnLine office, as well as banks and other institutions, never ask for information in this way, so students should be wary of emails that require such action.</p>

<h2 class="eh-header">Changing tactics</h2>
<p>The nature of hacking itself has changed within the past few years, Bollens said.</p>
<p>Physical theft, like stolen laptops, used to characterize the biggest security breaches, he said. When information was increasingly stored electronically, the threat then turned to database hacking.</p>
<p>Now, systems are much more encrypted. Because of difficulty with encryption, modern tactics tend to involve hackers &ldquo;phishing&rdquo; &ndash; trying to dupe network users &ndash; for user involvement.</p>
<p>&ldquo;They can&rsquo;t get to the data in (traditional) ways,&rdquo; Bollens said.&ldquo;(Phishing is) pervasive now, more recent even than in the past five years,&rdquo; he said.</p>
<p>Bollens pulled out his phone during an interview with the Daily Bruin to show that he had received a &ldquo;phishing&rdquo; scam email since the interview began.</p>
<p>Paul Rivers, interim chief information security officer at UC Berkeley, said IT officials are more concerned with the changing strategies of hacking attempts than the increasing number. This includes hackers targeting individuals or departments through "phishing" scams, rather than trying to get into a general database that is now encrypted.</p>
<p>&ldquo;What we&rsquo;re worried about, increasingly, is that the attacks have very strong motives,&rdquo; Rivers said. &ldquo;It&rsquo;s not just about getting in ... they&rsquo;ll use a number of different ways to penetrate into the campus network without being noticed ... they&rsquo;re willing to slowly go about getting (personal information).&rdquo;</p>

<h2 class="eh-header">Plan of action</h2>
<p>The UC has consistently modified its plan of action in case of a major breach of security. The UC Privacy and Data Security Incident Response Plan, last updated in 2011, contains a broad plan in the case of a security breach, detailing campus-to-campus contacts, and also outlining a notification email and steps to take according to California law.</p>
<p>Bollens said he thinks California has one of the most stringent data security laws in the nation. <a href="//www.leginfo.ca.gov/cgi-bin/calawquery?codesection=civ&codebody=&hits=20">California laws</a> stipulate conditions for disclosing breaches to affected individuals and lay out penalties for gaining access to information under false pretenses, among other provisions.</p>
<p>And the UC goes further, he said.</p>
<p>UCLA has a policy, Policy 420, drafted by Bollens, which details how to handle a breach at the campus level.</p>
<div class="eh-infobox eh-aside-right">
    <h3 class="eh-header">Response process to security breach at UCLA</h3>
    <ol>
        <li>Organization heads – the appropriate campus officials – assess the scope of the incident and decide whether an official breach has occurred.
        <li>IT officials within the appropriate campus department or organization immediately neutralize threat or shut down infected site.
        <li>Campus officials notify all persons whose personal information may have been affected or illegally obtained by an unauthorized source, either through hard copy or email.
        <li>Officials do in-depth post-mortum analysis of breach, to analyze what can be done to prevent it in future.
        <li>Any costs incurred by the university during the process are paid by the organization within which the breach originated.
    </ol>
    <p class="eh-caption">*If the breach is systemwide, follow measures outlined in UC Privacy and Data Security Incident Response Plan</p>
    <p class="eh-credit">Source: UCLA Administrative Policy 420. Compiled by Katherine Hafner, Bruin senior staff.</p>
</div>
<p>Other UC campuses have experienced breaches as well &ndash; each one serving as a wake-up call.</p>
<p>UC Berkeley&rsquo;s biggest breach occurred in 2009 at its medical center, when it was discovered that about 160,000 names, addresses and other pieces of personal information had been accessed and compromised through hacking into the system , Rivers said.</p>
<p>&ldquo;What we had been doing prior to that was inadequate,&rdquo; he said. "(It&rsquo;s) not negligence, it just takes a while to understand that there have been some significant changes to ... the approach to security.&rdquo;</p>
<p>At UC Berkeley, the campus-level group that is responsible for directing security efforts recently doubled its security budget from $1.5 to $3 million, Rivers said.</p>
<p>The equivalent group at UCLA has a budget of around $500,000 per year, Bollens said.</p>
<p>He added, however, that the organizational structure for dealing with IT security varies from campus to campus, and is especially dependent on the number of people involved because much of the funding goes to paying salaries.</p>
<p>There is also a contradiction between the openness universities try to uphold and the high-tech security that they require, Rivers said.</p>
<p>&ldquo;By design, we want it to be pretty open,&rdquo; Rivers said. &ldquo;Some systems are harder to protect than others, and (the university) doesn&rsquo;t have a super protected border.&rdquo;</p>
<p>Bollens said the UCLA Medical Center is surrounded by a giant firewall, anetwork security system that analyzes data and decides what is and is not allowed in.</p>
<p>UCLA does not have a firewall around all of its information, however, because of the openness the school tries to protect, he added.</p>
<p>The emergence of cloud computing services, like iCloud, which allow people to store their information online, is another factor that makes security increasingly difficult.</p>
<p>Thomas Trappler, director of software licensing for UCLA's Office of Information Technology, is an expert in the field of cloud computing. He consults with companies on the security risks of using such services.</p>
<p>Trappler said he has seen a paradigm shift while working with software licensing &ndash; companies increasingly turning to cloud-based storage or software for convenience. But there are inherent security risks.</p>
<p>He said UCLA is one example of an organization starting to use cloud computing services. UCLA students now use Gmail accounts instead of accounts created by the Bruin OnLine office, which means student information is stored by Google at an off-site location, rather than a location operated by UCLA.</p>
<p>&ldquo;(Traditionally), if something goes wrong you go down and fix it,&rdquo; Trappler said. &ldquo;When you move to cloud computing, it&rsquo;s moving to contractual ... somebody else is doing (maintenance) for you.&rdquo;</p>
<p>At UCLA, however, most information is still on campus-operated systems, though the university does use some cloud-based programs such as Blackboard Collaborate, an online platform for educational collaboration with services like web conferencing.</p>

<h2 class="eh-header">Minimizing the risk</h2>
<div class="eh-infobox eh-aside-right">
    <h3 class="eh-header">UCLA IT Security Office's tips for password security</h3>
    <ul>
        <li> Never give your password to anybody or write it down in a visible place. Password sharing is also a violation of the Bruin OnLine Acceptable Use Policy, and result in a loss of your account.
        <li>Change your password at least once every three to six months, or after you use a computer that is publicly accessible.
        <li>Don&rsquo;t choose a password that can be found in a dictionary in any language, or that uses personal information like birthdays or family member&rsquo;s names that are available on social networking sites.
        <li>Choose a long and complex password that uses a mix of capital and lowercase letters, numbers and special characters.
        <li>Don't choose a password made up of characters that are successive keys on the keyboard, like qwerty or 123.
    </ul>
    <p class="eh-credit">Source: UCLA Office of Information Technology Security. Compiled by Katherine Hafner, Bruin senior staff.</p>
</div>
<p>Michael Cherry, president of Cherry Biometrics, a Virginia-based consulting firm that advises on cyber security, said no organization or individual on the Internet should think they are that they are immune from hacking in the current era of new technology.</p>
<p>&ldquo;I don&rsquo;t think anybody (on the Internet) is hack-proof,&rdquo; Cherry said. &ldquo;Everyone&rsquo;s being hacked all the time, but they don&rsquo;t know it. ... They (realize) when someone takes a hundred million credit cards.&rdquo;</p>
<p>There are ways, though, like using an extranet &ndash; a computer network that controls access from the outside &ndash; to minimize the risk of hacking, Cherry said. Hackers have been around since the beginning of computer systems, but people are just starting to pay more attention.</p>
<p>Cherry said significantly bolstering security against hacking requires writing new, more complicated programs &ndash; and with that, significant funding.</p>
<p>Though Berkeley has increased its security budget, Rivers said the main factor for IT security officials to consider is remembering to be open and adapt to an increasingly changing cyber landscape.<br />&ldquo;I still think we&rsquo;re at risk ... it&rsquo;s just inherent,&rdquo; Rivers said. &ldquo;I think there is more that we can and must do.&rdquo;</p>

